import csv
import json
import time
from random import randint
from typing import Callable
from os import path

INPUT_FILE = "quadr_equ_input.csv"
OUTPUT_FILE = "quadr_equ_output.json"
EQUATION_COUNT = 5


def csv_file_gen(trinity_count:int, min_num:int, max_num:int, \
                file_name:str) -> None:
    '''- –ì–µ–Ω–µ—Ä–∞—Ü–∏—è csv —Ñ–∞–π–ª–∞ —Å —Ç—Ä–µ–º—è —Å–ª—É—á–∞–π–Ω—ã–º–∏ —á–∏—Å–ª–∞–º–∏ –≤ –∫–∞–∂–¥–æ–π —Å—Ç—Ä–æ–∫–µ.'''
    with open(file_name, 'w', newline='', encoding='utf-8') as f_write:
        csv_write = csv.writer(f_write, dialect='excel')
        for _ in range(trinity_count):
            num_1 = randint(min_num, max_num+1)
            num_2 = randint(min_num, max_num+1)
            num_3 = randint(min_num, max_num+1)
            csv_write.writerow([num_1, num_2, num_3])


def outer_input_deco(csv_file_name:str):
    if not path.isfile(csv_file_name):
        print("–û—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç csv-—Ñ–∞–π–ª –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö, –∏–¥—ë—Ç —Å–æ–∑–¥–∞–Ω–∏–µ...")
        csv_file_gen(EQUATION_COUNT, -20, 20+1, csv_file_name)
        print("...—Å–æ–∑–¥–∞–Ω–∏–µ csv-—Ñ–∞–π–ª–∞ –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö –∑–∞–≤–µ—Ä—à–µ–Ω–æ")
    def input_deco(func: Callable):
        def wrapper_inp(*args, **kwargs):
            # print(f'(wrapper_inp1) –ó–∞–ø—É—Å–∫ —Ñ—É–Ω–∫—Ü–∏–∏ {func.__name__} –≤ {time.time()}')
            # print('(wrapper_inp2)', csv_file_name)

            with open(csv_file_name, 'r', newline='') as f:
                csv_file = csv.reader(f)
                for line in csv_file:
                    arg_a = int(line[0])
                    arg_b = int(line[1])
                    arg_c = int(line[2])
                    # print(f'–ó–∞–ø—É—Å–∫ —Ñ—É–Ω–∫—Ü–∏–∏ "{func.__name__}"')
                    result = func(arg_a, arg_b, arg_c, **kwargs)    # –≤–æ—Ç –∑–¥–µ—Å—å –µ—ë –≤—ã–∑–æ–≤
                    # print(f'–†–µ–∑—É–ª—å—Ç–∞—Ç —Ñ—É–Ω–∫—Ü–∏–∏ "{func.__name__}": {result};  {arg_a=};  {arg_b=};  {arg_c=}\n')

            # print(f'(wrapper_inp3) –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ {func.__name__} –≤ {time.time()}')
            return result
        # print('–í–æ–∑–≤—Ä–∞—â–∞–µ–º –¥–µ–∫–æ—Ä–∞—Ç–æ—Ä-INP')
        return wrapper_inp
    return input_deco


def outer_output_deco(json_file_name:str):
    def output_deco(func: Callable):
        def wrapper_out(*args, **kwargs):
            # print('   –°—Ç–∞—Ä—Ç –≤—Ä–∞–ø–ø–µ—Ä–∞-OUT')
            # print(f'   –ó–∞–ø—É—Å–∫–∞—é  {func.__name__}  –∏–∑ –≤—Ä–∞–ø–ø–µ—Ä–∞-OUT')
            res = func(*args, **kwargs)

            if path.isfile(json_file_name):
                with open(json_file_name, 'r', encoding='utf-8') as f:
                    my_dict = json.load(f)
            else:
                my_dict = {}
            my_dict[f'{time.time()=}'] = (args[0], args[1], args[2], res)
            with open(json_file_name, 'w', encoding='utf-8') as f:
                json.dump(my_dict, f, indent=2, ensure_ascii=False)

            # print(f'   –†–µ–∑—É–ª—å—Ç–∞—Ç —Ñ—É–Ω–∫—Ü–∏–∏ "{func.__name__}": {res};  {args[0]=};  {args[1]=};  {args[2]=}')
            # print(f'   –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ –≤—Ä–∞–ø–ø–µ—Ä–∞-OUT')
            return res
        # print('–í–æ–∑–≤—Ä–∞—â–∞–µ–º –¥–µ–∫–æ—Ä–∞—Ç–æ—Ä-OUT')
        return wrapper_out
    return output_deco


